package cla.edg.actionpattern.favorite;

public class FavoriteAPInfo {
	String actorModelName;
	String targetModelName;
	String recordModelName;
	
	String basePackageName;
	String userContextClassName;
	public String getActorModelName() {
		return actorModelName;
	}
	public void setActorModelName(String actorModelName) {
		this.actorModelName = actorModelName;
	}
	public String getTargetModelName() {
		return targetModelName;
	}
	public void setTargetModelName(String targetModelName) {
		this.targetModelName = targetModelName;
	}
	public String getRecordModelName() {
		return recordModelName;
	}
	public void setRecordModelName(String recordModelName) {
		this.recordModelName = recordModelName;
	}
	public String getBasePackageName() {
		return basePackageName;
	}
	public void setBasePackageName(String basePackageName) {
		this.basePackageName = basePackageName;
	}
	public String getUserContextClassName() {
		return userContextClassName;
	}
	public void setUserContextClassName(String userContextClassName) {
		this.userContextClassName = userContextClassName;
	}
	
	
}
